# Упорядоченные цифры 🌶
# Дано натуральное число.
# Напишите программу, которая определяет, является ли последовательность его цифр
# при просмотре справа налево упорядоченной по неубыванию.
# Формат входных данных
# На вход программе подается одно натуральное число.
# Формат выходных данных
# Программа должна вывести «YES» если последовательность его цифр при просмотре справа налево
# является упорядоченной по неубыванию и «NO» в противном случае

n = int(input())

n_bool = True
while n > 9:
    m = n % 10              # последняя
    k = n // 10 % 10        # предпоследняя
    if k >= m:
        n_bool *= True
    else:
        n_bool *= False
    n = n // 10
if n_bool == True:
    print('YES')
else:
    print('NO')
